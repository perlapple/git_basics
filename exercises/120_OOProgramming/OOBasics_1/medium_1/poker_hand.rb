load 'cards.rb'

class PokerHand
  attr_accessor :hand

  def initialize(deck_or_hand)
    if deck_or_hand.is_a? Deck
      @hand = deck_or_hand.cards.sample(5)
    else
      @hand = deck_or_hand
    end
  end

  def print
    @hand
  end

  def evaluate

    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def royal_flush?
    straight_flush? && @hand.sort.map(&:rank).first == 10
  end

  def straight_flush?
    straight? && flush?
  end

  def four_of_a_kind?
    group_by_rank(4).one?
  end

  def full_house?
    three_of_a_kind? && pair?
  end

  def flush?
    @hand.map(&:suit).uniq.one?
  end

  def straight?
    @hand.map(&:ranking_to_numeric).sort.each_cons(2).all? { |cons| cons[1] - cons[0] == 1 }
  end

  def three_of_a_kind?
    group_by_rank(3).one?
  end

  def two_pair?
    group_by_rank(2).length == 2
  end

  def pair?
    group_by_rank(2).one?
  end

  def group_by_rank(num)
    @hand.group_by(&:rank).select{ |rank, cards| cards.length == num }
  end

  def sort
    @hand.sort!
  end
end

